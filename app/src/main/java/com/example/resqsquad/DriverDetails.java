package com.example.resqsquad;

public class DriverDetails {
    public String name;
    public String phno;
    public String carNumberPlate;
    public String carModel;

    public DriverDetails() {
    }

    public DriverDetails(String name, String phno, String carNumberPlate, String carModel) {
        this.name = name;
        this.phno = phno;
        this.carNumberPlate = carNumberPlate;
        this.carModel = carModel;
    }
}